  @override
  void initState() {
    super.initState();
    fetchDriverInfo();
  }

  void fetchDriverInfo() async {
    DatabaseReference driverRef = driversRef.child(currentFirebaseUser!.uid);
    DataSnapshot snapshot = await driverRef.once().then(
      (value) => value.snapshot,
    );

    if (snapshot.value != null) {
      Map data = snapshot.value as Map;

      setState(() {
        driverName = data["name"] ?? "Driver";
        carType = data["car_details"]?["car_type"] ?? "";
      });
    }
  }

  void logout() async {
    await FirebaseAuth.instance.signOut();
    Navigator.pushAndRemoveUntil(
      context,
      MaterialPageRoute(builder: (context) => const Loginscreen()),
      (route) => false,
    );
  }

  @override
  Widget build(BuildContext context) {
    return Stack(
      children: [
        GoogleMap(
          mapType: MapType.normal,
          myLocationEnabled: true,
          initialCameraPosition: _kGooglePlex,
          onMapCreated: (GoogleMapController controller) {
            _controllerGoogleMap.complete(controller);
            newGoogleMapController = controller;
            // Set the map style to black theme
            blackThemeGoogleMap();
          },
        ),
      ],
    );
  }
